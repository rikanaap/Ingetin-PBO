    private void setData(){
        try {
            ResultSet motivasi = database_motivasi.cariMotivasi(id);
            if (motivasi != null && motivasi.next()) { 
                name_input.setText(motivasi.getString("name"));
                parameter_input.setText(String.valueOf(motivasi.getInt("parameter")));
            } else {
                System.out.println("Data motivasi dengan ID tersebut tidak ditemukan.");
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }   
    }

    private void setUkuranLokasi(){
        // Ambil ukuran layar user (monitor)
        java.awt.Dimension screenSize = java.awt.Toolkit.getDefaultToolkit().getScreenSize();
        this.pack();
        
        int frameWidth = this.getSize().width;
        System.out.println(frameWidth);
        System.out.println(this.getSize().height);

        // Hitung posisi X agar mepet ke kanan
        int x = screenSize.width - frameWidth;
        int y = 0; // 0 berarti mepet ke atas

        this.setLocation(x, y);
    }
    
